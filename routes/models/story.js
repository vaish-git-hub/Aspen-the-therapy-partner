const mongoose = require('mongoose');

// Define the schema for the Story model
const storySchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  content: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

// Create a model from the schema
const Story = mongoose.model('Story', storySchema);

module.exports = Story;
